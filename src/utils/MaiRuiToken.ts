  private static readonly MAX_USAGE = 50;
  
  /**
   * Check and reset index at midnight
   */
  private static checkAndResetIndex(): void {
    const currentDate = new Date().toDateString();
    // console.log(currentDate, this.lastResetDate);
    if (currentDate !== this.lastResetDate) {
      this.currentIndex = 0;
      this.tokenUsage = {};
      this.lastResetDate = currentDate;
    }
  }
  
  /**
   * Check if current token has exceeded usage limit
   */
  private static checkTokenUsage(): void {
    const currentToken = this.tokens[this.currentIndex];
    this.tokenUsage[currentToken] = (this.tokenUsage[currentToken] || 0) + 1;
    
    if (this.tokenUsage[currentToken] >= this.MAX_USAGE) {
      this.updateToken();
    }
  }
  
  /**
   * Get current token
   */
  static getCurrentToken(): string {
    this.checkAndResetIndex();
    this.checkTokenUsage();
    return this.tokens[this.currentIndex];
  }
  
  /**
   * Update to next token
   */
  static updateToken(): string {
    this.checkAndResetIndex();
    // Mark current token as fully used
    const currentToken = this.tokens[this.currentIndex];
    this.tokenUsage[currentToken] = this.MAX_USAGE;
  
    this.currentIndex = (this.currentIndex + 1) % this.tokens.length;
    const newToken = this.tokens[this.currentIndex];
    this.tokenUsage[newToken] = 0;
    return this.getCurrentToken();
  }
